let fruit = prompt("Know your color");
switch(fruit){
    case "apple":
        console.log(`${fruit} is red color` );
        break;
    case "orange":
        console.log(`${fruit} is orange color` );
        break;
    case "mango":
        console.log(`${fruit} is yellow color` );
        break;
    case "grapes":
        console.log(`${fruit} is green color` );
        break;
    default:
        console.log(`${fruit} is not a fruit`);
}
